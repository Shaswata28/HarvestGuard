/**
 * Get Bangla name for a location, fallback to English if not found
 */
export function getBanglaName(englishName: string, type: 'division' | 'district' | 'upazila'): string {
  const translations = type === 'division' 
    ? DIVISION_TRANSLATIONS 
    : type === 'district' 
    ? DISTRICT_TRANSLATIONS 
    : UPAZILA_TRANSLATIONS;
  
  return translations[englishName] || englishName;
}

/**
 * Format location name for display (Bangla + English)
 */
export function formatLocationName(englishName: string, type: 'division' | 'district' | 'upazila', showBoth: boolean = true): string {
  const banglaName = getBanglaName(englishName, type);
  
  if (!showBoth || banglaName === englishName) {
    return banglaName;
  }
  
  return `${banglaName} (${englishName})`;
}
